package operators;

public class BasicAddition {

	public static void main(String[] args) {

		/*
		 * Approach 1: Directly we can perform operation using addition operator (+) in
		 * the print statement.
		 */
		System.out.println(20 + 30);

		int value1 = 20;
		int value2 = 30;

		/*
		 * Approach 2: Perform the addition Operation and assign to a variable. Print
		 * the Statement
		 */
		int sumOftheValues = value1 + value2;
		System.out.println("Result1: " + sumOftheValues);

		/*
		 * Approach 3: Perform the addition operation using by directly adding the
		 * values in the Print statement
		 */
		System.out.println("Result2: "+(value1+value2));
		
	}

}
